# Maior e Menor Número:
# Programe um software que recebe três números e cria duas funções: uma que retorna o maior número e outra que retorna o menor número.
def maiorNumero(a,b,c):
    listie = []
    listie.append(a)
    listie.append(b)
    listie.append(c)
    print(max(listie))

def menorNumero(a,b,c):
    listie = []
    listie.append(a)
    listie.append(b)
    listie.append(c)
    print(min(listie))

maiorNumero(11,22,30)
menorNumero(3213232131,46546,54643)